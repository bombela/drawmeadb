#include "SQLPrint.h"

#include <iostream>
#include <iterator>

#include "DateTime.h"

using namespace std;

namespace MeriseAcide
{

SQLPrint::SQLPrint(void): mOStream(0) { }

SQLPrint::SQLPrint(const std::string& FileName)
{
	Open(FileName);
}

SQLPrint::SQLPrint(std::ostream& os)
{
	Open(os);
}

SQLPrint::~SQLPrint(void)
{
	Close();
}

bool SQLPrint::Open(const std::string& FileName)
{
	Close();

	mFileName = FileName;
	mFileStream.open( mFileName.c_str() );

	if ( mFileStream.is_open() )
	{
		mPutComa = false;
		mOStream = &mFileStream;
		PutHeader();
	}
	
	return IsOpen();
}

bool SQLPrint::Open(std::ostream& os)
{
	Close();

	mOStream = &os;
	if (mOStream)
	{
		mPutComa = false;
		PutHeader();
	}
	return IsOpen();
}

void SQLPrint::Close()
{
	if ( IsOpen() )
	{
		mOStream = 0;
		mFileStream.close();
		mFileName = "";
	}
}

bool SQLPrint::IsOpen(void) const
{
	return mOStream;
}

void SQLPrint::NeedComa(void)
{
	mPutComa = true;
}

void SQLPrint::PutComaIfNeeded(void)
{
	if (mPutComa)
	{
		mPutComa = false;
		*mOStream << ",\n";
	}
}

void SQLPrint::ResetComa()
{
	mPutComa = false;
}

void SQLPrint::BreakLine()
{
	*mOStream << "\n";
}

void SQLPrint::PutHeader(void)
{
	DateTime dt;
	dt.LoadFromNow();

	PutComment("Generated by MeriseAcide. ");
	PutComment(dt.Fmt("%F %H:%M %Z") + ".");
	BreakLine(); 
}

}
